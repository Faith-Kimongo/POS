    {
        //
        $contact = new SubCategory([
            'name' => $request->name,
            'description'=>$request->description,
            'category_id'=>$request->id,
            'hotel_id'=>Auth::id(),
            'branch_id'=>Session::get('branch'),
        ]);
        $contact->save();  
        return back()->with('status','Sub Category  added succefully!');
